#include "libft.h"



void	*ft_memset(void *s1, int c, size_t n)
{
	unsigned char	*p1;

	p1 = s1;
	while (n > 0)
	{
		*p1 = (unsigned char)c;
		p1++;
		n--;
	}
	return (s1);
}
