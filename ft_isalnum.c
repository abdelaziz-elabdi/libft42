#include "libft.h"

int	ft_isalnum(int c)
{
	int	m;

	m = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') 
		|| (c >= '0' && c <= '9'))
	{
		m = 1;
	}
	return (m);
}
